#include "main.h"

/**
 * _abs - function that computes the absolute value of an integer
 * @v: the parameter to be checked
 *
 * Return: 1 if result is greater than zero
 * 0 if result is zero
 * 1 if result is less than zero
 */
int _abs(int v)
{
	if (v >= 0)
	{
	return (v);
	}
	else
	{
	return (-v);
	}
}
